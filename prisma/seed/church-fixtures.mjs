export async function seedChurchFixtures(prisma) {
  for (const fixture of CHURCH_FIXTURES) {
    const existing = await prisma.church.findUnique({
      where: { website: fixture.website },
      select: { id: true },
    });
    const churchId = existing?.id ?? fixture.id;
    const data = toChurchData(fixture);

    if (existing) {
      await prisma.church.update({ where: { id: churchId }, data });
      await prisma.churchAddress.deleteMany({ where: { churchId } });
      await prisma.churchServiceTime.deleteMany({ where: { churchId } });
      await prisma.churchEvaluation.deleteMany({ where: { churchId } });
    } else {
      await prisma.church.create({ data: { id: fixture.id, ...data } });
    }

    for (const address of fixture.addresses) {
      await prisma.churchAddress.create({
        data: {
          id: address.id,
          churchId,
          street1: address.street1,
          street2: address.street2,
          city: address.city,
          state: address.state,
          postCode: address.postCode,
          latitude: address.latitude,
          longitude: address.longitude,
          sourceUrl: address.sourceUrl,
          isPrimary: address.isPrimary,
          createdAt: toDate(address.createdAt),
          updatedAt: toDate(address.updatedAt),
        },
      });
    }

    for (const serviceTime of fixture.serviceTimes) {
      await prisma.churchServiceTime.create({
        data: {
          id: serviceTime.id,
          churchId,
          label: serviceTime.label,
          createdAt: toDate(serviceTime.createdAt),
          updatedAt: toDate(serviceTime.updatedAt),
        },
      });
    }

    for (const evaluation of fixture.evaluations) {
      await prisma.churchEvaluation.create({
        data: {
          id: evaluation.id,
          churchId,
          rawEvaluation: evaluation.rawEvaluation,
          badges: evaluation.badges,
          secondary: evaluation.secondary,
          tertiary: evaluation.tertiary,
          coreOnSiteCount: evaluation.coreOnSiteCount,
          coreTotalCount: evaluation.coreTotalCount,
          coverageRatio: evaluation.coverageRatio,
          status: evaluation.status,
          coreTrinity: evaluation.coreTrinity,
          coreGospel: evaluation.coreGospel,
          coreJustificationByFaith: evaluation.coreJustificationByFaith,
          coreChristDeityHumanity: evaluation.coreChristDeityHumanity,
          coreScriptureAuthority: evaluation.coreScriptureAuthority,
          coreIncarnationVirginBirth: evaluation.coreIncarnationVirginBirth,
          coreAtonementNecessary: evaluation.coreAtonementNecessary,
          coreResurrectionOfJesus: evaluation.coreResurrectionOfJesus,
          coreReturnAndJudgment: evaluation.coreReturnAndJudgment,
          coreCharacterOfGod: evaluation.coreCharacterOfGod,
          createdAt: toDate(evaluation.createdAt),
        },
      });
    }
  }

  return { fixtureCount: CHURCH_FIXTURES.length };
}
